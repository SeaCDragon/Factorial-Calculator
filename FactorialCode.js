var facArray = [];
var fac = 0;
var facValue = 0;
function start(){
    getFactorial();
    findFacMultiples();
    findFacValue();
    println(fac);
    println(facArray);
    print(facValue);
}
function getFactorial(){
    fac = readInt("What is your factorial?");
}
function findFacMultiples(){
    for(var i = 1; i <= (fac); i++){
        facArray.push(i);
    }
}
function findFacValue(){
    var product = 1;
    for(var i = 0; i < facArray.length; i++){
        product *= facArray[i];}
        facValue = product;
}
